      buildList<NetworkIdentityManagementClusterActiveNetworkIdentityStruct> {
        tlvReader.enterArray(AnonymousTag)
        while (!tlvReader.isEndOfContainer()) {
          add(
            NetworkIdentityManagementClusterActiveNetworkIdentityStruct.fromTlv(
