  suspend fun proxyConnectRequest(
    address: ByteArray?,
    transport: UByte,
    discriminator: UShort,
    vendorID: UShort,
    productID: UShort,
    timeout: UShort,
    wiFiBand: UShort?,
    timedInvokeTimeout: Duration? = null,
  ): ProxyConnectResponse {
    val commandId: UInt = 0u

    val tlvWriter = TlvWriter()
    tlvWriter.startStructure(AnonymousTag)

    val TAG_ADDRESS_REQ: Int = 0
    address?.let { tlvWriter.put(ContextSpecificTag(TAG_ADDRESS_REQ), address) }

    val TAG_TRANSPORT_REQ: Int = 1
    tlvWriter.put(ContextSpecificTag(TAG_TRANSPORT_REQ), transport)

    val TAG_DISCRIMINATOR_REQ: Int = 2
    tlvWriter.put(ContextSpecificTag(TAG_DISCRIMINATOR_REQ), discriminator)

    val TAG_VENDOR_ID_REQ: Int = 3
    tlvWriter.put(ContextSpecificTag(TAG_VENDOR_ID_REQ), vendorID)

    val TAG_PRODUCT_ID_REQ: Int = 4
    tlvWriter.put(ContextSpecificTag(TAG_PRODUCT_ID_REQ), productID)

    val TAG_TIMEOUT_REQ: Int = 5
    tlvWriter.put(ContextSpecificTag(TAG_TIMEOUT_REQ), timeout)

    val TAG_WI_FI_BAND_REQ: Int = 6
    wiFiBand?.let { tlvWriter.put(ContextSpecificTag(TAG_WI_FI_BAND_REQ), wiFiBand) }
    tlvWriter.endStructure()

    val request: InvokeRequest =
      InvokeRequest(
        CommandPath(endpointId, clusterId = CLUSTER_ID, commandId),
        tlvPayload = tlvWriter.getEncoded(),
        timedRequest = timedInvokeTimeout,
      )

    val response: InvokeResponse = controller.invoke(request)
    logger.log(Level.FINE, "Invoke command succeeded: ${response}")

    val tlvReader = TlvReader(response.payload)
    tlvReader.enterStructure(AnonymousTag)
    val TAG_SESSION_ID: Int = 0
    var sessionID_decoded: UShort? = null

    while (!tlvReader.isEndOfContainer()) {
      val tag = tlvReader.peekElement().tag

      if (tag == ContextSpecificTag(TAG_SESSION_ID)) {
        sessionID_decoded = tlvReader.getUShort(tag)
      } else {
        tlvReader.skipElement()
      }
    }

    if (sessionID_decoded == null) {
      throw IllegalStateException("sessionID not found in TLV")
    }

    tlvReader.exitContainer()

    return ProxyConnectResponse(sessionID_decoded)
  }

