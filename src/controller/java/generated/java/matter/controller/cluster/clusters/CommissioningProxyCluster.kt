  suspend fun proxyScanRequest(
    transport: UByte,
    wiFiBands: UShort?,
    timedInvokeTimeout: Duration? = null,
  ): ProxyScanResponse {
    val commandId: UInt = 3u

    val tlvWriter = TlvWriter()
    tlvWriter.startStructure(AnonymousTag)

    val TAG_TRANSPORT_REQ: Int = 0
    tlvWriter.put(ContextSpecificTag(TAG_TRANSPORT_REQ), transport)

    val TAG_WI_FI_BANDS_REQ: Int = 1
    wiFiBands?.let { tlvWriter.put(ContextSpecificTag(TAG_WI_FI_BANDS_REQ), wiFiBands) }
    tlvWriter.endStructure()

    val request: InvokeRequest =
      InvokeRequest(
        CommandPath(endpointId, clusterId = CLUSTER_ID, commandId),
        tlvPayload = tlvWriter.getEncoded(),
        timedRequest = timedInvokeTimeout,
      )

    val response: InvokeResponse = controller.invoke(request)
    logger.log(Level.FINE, "Invoke command succeeded: ${response}")

    val tlvReader = TlvReader(response.payload)
    tlvReader.enterStructure(AnonymousTag)
    val TAG_NUMBER_OF_RESULTS: Int = 0
    var numberOfResults_decoded: UByte? = null

    val TAG_PROXY_SCAN_RESULT: Int = 1
    var proxyScanResult_decoded: List<CommissioningProxyClusterScanResultStruct>? = null

    while (!tlvReader.isEndOfContainer()) {
      val tag = tlvReader.peekElement().tag

      if (tag == ContextSpecificTag(TAG_NUMBER_OF_RESULTS)) {
        numberOfResults_decoded = tlvReader.getUByte(tag)
      } else if (tag == ContextSpecificTag(TAG_PROXY_SCAN_RESULT)) {
        proxyScanResult_decoded =
          buildList<CommissioningProxyClusterScanResultStruct> {
            tlvReader.enterArray(tag)
            while (!tlvReader.isEndOfContainer()) {
              add(CommissioningProxyClusterScanResultStruct.fromTlv(AnonymousTag, tlvReader))
            }
            tlvReader.exitContainer()
          }
      } else {
        tlvReader.skipElement()
      }
    }

    if (numberOfResults_decoded == null) {
      throw IllegalStateException("numberOfResults not found in TLV")
    }

    if (proxyScanResult_decoded == null) {
      throw IllegalStateException("proxyScanResult not found in TLV")
    }

    tlvReader.exitContainer()

    return ProxyScanResponse(numberOfResults_decoded, proxyScanResult_decoded)
  }

