    val TAG_MODE_REQ: Int = 0
    tlvWriter.put(ContextSpecificTag(TAG_MODE_REQ), mode)

    val TAG_AMOUNT_REQ: Int = 1
    tlvWriter.put(ContextSpecificTag(TAG_AMOUNT_REQ), amount)
    tlvWriter.endStructure()

    val request: InvokeRequest =
      InvokeRequest(
