  suspend fun testEventTrigger(
    enableKey: ByteArray,
    eventTrigger: ULong,
    timedInvokeTimeout: Duration? = null
  ) {
    val commandId: UInt = 0u

    val tlvWriter = TlvWriter()
    tlvWriter.startStructure(AnonymousTag)

    val TAG_ENABLE_KEY_REQ: Int = 0
    tlvWriter.put(ContextSpecificTag(TAG_ENABLE_KEY_REQ), enableKey)

    val TAG_EVENT_TRIGGER_REQ: Int = 1
    tlvWriter.put(ContextSpecificTag(TAG_EVENT_TRIGGER_REQ), eventTrigger)
    tlvWriter.endStructure()

    val request: InvokeRequest =
      InvokeRequest(
        CommandPath(endpointId, clusterId = CLUSTER_ID, commandId),
        tlvPayload = tlvWriter.getEncoded(),
        timedRequest = timedInvokeTimeout
      )

    val response: InvokeResponse = controller.invoke(request)
    logger.log(Level.FINE, "Invoke command succeeded: ${response}")
  }

  suspend fun timeSnapshot(timedInvokeTimeout: Duration? = null): TimeSnapshotResponse {
    val commandId: UInt = 1u

    val tlvWriter = TlvWriter()
    tlvWriter.startStructure(AnonymousTag)
    tlvWriter.endStructure()

    val request: InvokeRequest =
      InvokeRequest(
        CommandPath(endpointId, clusterId = CLUSTER_ID, commandId),
        tlvPayload = tlvWriter.getEncoded(),
        timedRequest = timedInvokeTimeout
      )

    val response: InvokeResponse = controller.invoke(request)
    logger.log(Level.FINE, "Invoke command succeeded: ${response}")

    val tlvReader = TlvReader(response.payload)
    tlvReader.enterStructure(AnonymousTag)
    val TAG_SYSTEM_TIME_MS: Int = 0
    var systemTimeMs_decoded: ULong? = null

    val TAG_POSIX_TIME_MS: Int = 1
    var posixTimeMs_decoded: ULong? = null

    while (!tlvReader.isEndOfContainer()) {
      val tag = tlvReader.peekElement().tag

      if (tag == ContextSpecificTag(TAG_SYSTEM_TIME_MS)) {
        systemTimeMs_decoded = tlvReader.getULong(tag)
      }

      if (tag == ContextSpecificTag(TAG_POSIX_TIME_MS)) {
        posixTimeMs_decoded =
          if (tlvReader.isNull()) {
            tlvReader.getNull(tag)
            null
          } else {
            if (!tlvReader.isNull()) {
              tlvReader.getULong(tag)
            } else {
              tlvReader.getNull(tag)
              null
            }
          }
      } else {
        tlvReader.skipElement()
      }
    }

    if (systemTimeMs_decoded == null) {
      throw IllegalStateException("systemTimeMs not found in TLV")
    }

    tlvReader.exitContainer()

    return TimeSnapshotResponse(systemTimeMs_decoded, posixTimeMs_decoded)
  }

  suspend fun payloadTestRequest(
    enableKey: ByteArray,
    value: UByte,
    count: UShort,
    timedInvokeTimeout: Duration? = null
  ): PayloadTestResponse {
    val commandId: UInt = 3u

    val tlvWriter = TlvWriter()
    tlvWriter.startStructure(AnonymousTag)

    val TAG_ENABLE_KEY_REQ: Int = 0
    tlvWriter.put(ContextSpecificTag(TAG_ENABLE_KEY_REQ), enableKey)

    val TAG_VALUE_REQ: Int = 1
    tlvWriter.put(ContextSpecificTag(TAG_VALUE_REQ), value)

    val TAG_COUNT_REQ: Int = 2
    tlvWriter.put(ContextSpecificTag(TAG_COUNT_REQ), count)
    tlvWriter.endStructure()

    val request: InvokeRequest =
      InvokeRequest(
        CommandPath(endpointId, clusterId = CLUSTER_ID, commandId),
        tlvPayload = tlvWriter.getEncoded(),
        timedRequest = timedInvokeTimeout
      )

    val response: InvokeResponse = controller.invoke(request)
    logger.log(Level.FINE, "Invoke command succeeded: ${response}")

    val tlvReader = TlvReader(response.payload)
    tlvReader.enterStructure(AnonymousTag)
    val TAG_PAYLOAD: Int = 0
    var payload_decoded: ByteArray? = null

    while (!tlvReader.isEndOfContainer()) {
      val tag = tlvReader.peekElement().tag

      if (tag == ContextSpecificTag(TAG_PAYLOAD)) {
        payload_decoded = tlvReader.getByteArray(tag)
      } else {
        tlvReader.skipElement()
      }
    }

    if (payload_decoded == null) {
      throw IllegalStateException("payload not found in TLV")
    }

    tlvReader.exitContainer()

    return PayloadTestResponse(payload_decoded)
  }

  suspend fun readNetworkInterfacesAttribute(): NetworkInterfacesAttribute {
    val ATTRIBUTE_ID: UInt = 0u

    val attributePath =
      AttributePath(endpointId = endpointId, clusterId = CLUSTER_ID, attributeId = ATTRIBUTE_ID)

    val readRequest = ReadRequest(eventPaths = emptyList(), attributePaths = listOf(attributePath))

    val response = controller.read(readRequest)

    if (response.successes.isEmpty()) {
      logger.log(Level.WARNING, "Read command failed")
      throw IllegalStateException("Read command failed with failures: ${response.failures}")
    }

    logger.log(Level.FINE, "Read command succeeded")

    val attributeData =
      response.successes.filterIsInstance<ReadData.Attribute>().firstOrNull {
        it.path.attributeId == ATTRIBUTE_ID
      }

    requireNotNull(attributeData) { "Networkinterfaces attribute not found in response" }

    // Decode the TLV data into the appropriate type
    val tlvReader = TlvReader(attributeData.data)
    val decodedValue: List<GeneralDiagnosticsClusterNetworkInterface> =
      buildList<GeneralDiagnosticsClusterNetworkInterface> {
        tlvReader.enterArray(AnonymousTag)
        while (!tlvReader.isEndOfContainer()) {
          add(GeneralDiagnosticsClusterNetworkInterface.fromTlv(AnonymousTag, tlvReader))
        }
        tlvReader.exitContainer()
      }

    return NetworkInterfacesAttribute(decodedValue)
  }

  suspend fun subscribeNetworkInterfacesAttribute(
    minInterval: Int,
    maxInterval: Int
  ): Flow<NetworkInterfacesAttributeSubscriptionState> {
    val ATTRIBUTE_ID: UInt = 0u
    val attributePaths =
      listOf(
        AttributePath(endpointId = endpointId, clusterId = CLUSTER_ID, attributeId = ATTRIBUTE_ID)
      )

    val subscribeRequest: SubscribeRequest =
      SubscribeRequest(
        eventPaths = emptyList(),
        attributePaths = attributePaths,
        minInterval = Duration.ofSeconds(minInterval.toLong()),
        maxInterval = Duration.ofSeconds(maxInterval.toLong())
      )

    return controller.subscribe(subscribeRequest).transform { subscriptionState ->
      when (subscriptionState) {
        is SubscriptionState.SubscriptionErrorNotification -> {
          emit(
            NetworkInterfacesAttributeSubscriptionState.Error(
              Exception(
                "Subscription terminated with error code: ${subscriptionState.terminationCause}"
              )
            )
          )
        }
        is SubscriptionState.NodeStateUpdate -> {
          val attributeData =
            subscriptionState.updateState.successes
              .filterIsInstance<ReadData.Attribute>()
              .firstOrNull { it.path.attributeId == ATTRIBUTE_ID }

          requireNotNull(attributeData) {
            "Networkinterfaces attribute not found in Node State update"
          }

          // Decode the TLV data into the appropriate type
          val tlvReader = TlvReader(attributeData.data)
          val decodedValue: List<GeneralDiagnosticsClusterNetworkInterface> =
            buildList<GeneralDiagnosticsClusterNetworkInterface> {
              tlvReader.enterArray(AnonymousTag)
              while (!tlvReader.isEndOfContainer()) {
                add(GeneralDiagnosticsClusterNetworkInterface.fromTlv(AnonymousTag, tlvReader))
              }
              tlvReader.exitContainer()
            }

          emit(NetworkInterfacesAttributeSubscriptionState.Success(decodedValue))
        }
        SubscriptionState.SubscriptionEstablished -> {
          emit(NetworkInterfacesAttributeSubscriptionState.SubscriptionEstablished)
        }
      }
    }
  }

