  suspend fun requestSharedFiles(
    clientName: String,
    requestID: UShort,
    supportedMimeTypes: List<String>?,
    timedInvokeTimeout: Duration? = null,
  ) {
    val commandId: UInt = 3u

    val tlvWriter = TlvWriter()
    tlvWriter.startStructure(AnonymousTag)

    val TAG_CLIENT_NAME_REQ: Int = 0
    tlvWriter.put(ContextSpecificTag(TAG_CLIENT_NAME_REQ), clientName)

    val TAG_REQUEST_ID_REQ: Int = 1
    tlvWriter.put(ContextSpecificTag(TAG_REQUEST_ID_REQ), requestID)

    val TAG_SUPPORTED_MIME_TYPES_REQ: Int = 2
    supportedMimeTypes?.let {
      tlvWriter.startArray(ContextSpecificTag(TAG_SUPPORTED_MIME_TYPES_REQ))
      for (item in supportedMimeTypes.iterator()) {
        tlvWriter.put(AnonymousTag, item)
      }
      tlvWriter.endArray()
    }
    tlvWriter.endStructure()

    val request: InvokeRequest =
      InvokeRequest(
        CommandPath(endpointId, clusterId = CLUSTER_ID, commandId),
        tlvPayload = tlvWriter.getEncoded(),
