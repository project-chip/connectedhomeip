  suspend fun registerClient(
    checkInNodeID: ULong,
    monitoredSubject: ULong,
    key: ByteArray,
    verificationKey: ByteArray?,
    timedInvokeTimeoutMs: Int? = null
  ): RegisterClientResponse {
    val commandId: UInt = 0u
    val timeoutMs: Duration =
      timedInvokeTimeoutMs?.let { Duration.ofMillis(it.toLong()) } ?: Duration.ZERO

    val tlvWriter = TlvWriter()
    tlvWriter.startStructure(AnonymousTag)

    val TAG_CHECK_IN_NODE_I_D_REQ: Int = 0
    tlvWriter.put(ContextSpecificTag(TAG_CHECK_IN_NODE_I_D_REQ), checkInNodeID)

    val TAG_MONITORED_SUBJECT_REQ: Int = 1
    tlvWriter.put(ContextSpecificTag(TAG_MONITORED_SUBJECT_REQ), monitoredSubject)

    val TAG_KEY_REQ: Int = 2
    tlvWriter.put(ContextSpecificTag(TAG_KEY_REQ), key)

    val TAG_VERIFICATION_KEY_REQ: Int = 3
    verificationKey?.let {
      tlvWriter.put(ContextSpecificTag(TAG_VERIFICATION_KEY_REQ), verificationKey)
    }
    tlvWriter.endStructure()

    val request: InvokeRequest =
      InvokeRequest(
        CommandPath(endpointId, clusterId = CLUSTER_ID, commandId),
        tlvPayload = tlvWriter.getEncoded(),
        timedRequest = timeoutMs
      )

    val response: InvokeResponse = controller.invoke(request)
    logger.log(Level.INFO, "Invoke command succeeded: ${response}")

    val tlvReader = TlvReader(response.payload)
    tlvReader.enterStructure(AnonymousTag)
    val TAG_I_C_D_COUNTER: Int = 0
    val ICDCounter_decoded = tlvReader.getUInt(ContextSpecificTag(TAG_I_C_D_COUNTER))
    tlvReader.exitContainer()

    return RegisterClientResponse(ICDCounter_decoded)
  }

