      requireNotNull(attributeData) { "Registeredclients attribute not found in response" }

      // Decode the TLV data into the appropriate type
      val tlvReader = TlvReader(attributeData.data)
      val decodedValue: List<IcdManagementClusterMonitoringRegistrationStruct>? =
        if (tlvReader.isNextTag(AnonymousTag)) {
          buildList<IcdManagementClusterMonitoringRegistrationStruct> {
            tlvReader.enterArray(AnonymousTag)
            while (!tlvReader.isEndOfContainer()) {
              add(IcdManagementClusterMonitoringRegistrationStruct.fromTlv(AnonymousTag, tlvReader))
            }
            tlvReader.exitContainer()
          }
        } else {
          null
        }

      return RegisteredClientsAttribute(decodedValue)
    } else {
      logger.log(Level.WARNING, "Read command failed")
      throw IllegalStateException("Read command failed with failures: ${response.failures}")
