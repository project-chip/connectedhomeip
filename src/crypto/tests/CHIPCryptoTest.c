#define test_aes_ctr(key, iv, plaintext, ciphertext)                                                                               \
    {                                                                                                                              \
        CHIP_aes_ctr_ctx ctx;                                                                                                      \
        CHIP_aes_ctr_init(&ctx, key, sizeof(key), iv);                                                                             \
        uint8_t c[sizeof ciphertext], p[sizeof plaintext];                                                                         \
        CHIP_aes_ctr_encrypt(&ctx, c, plaintext, sizeof plaintext);                                                                \
        CHIP_aes_ctr_done(&ctx);                                                                                                   \
        CHIP_aes_ctr_init(&ctx, key, sizeof(key), iv);                                                                             \
        CHIP_aes_ctr_decrypt(&ctx, p, c, sizeof c);                                                                                \
        CHIP_aes_ctr_done(&ctx);                                                                                                   \
        nlASSERT(!memcmp(c, ciphertext, sizeof c));                                                                                \
        nlASSERT(!memcmp(p, plaintext, sizeof p));                                                                                 \
        CHIP_aes_ctr_init(&ctx, key, sizeof(key), iv);                                                                             \
        CHIP_aes_ctr_encrypt(&ctx, c, plaintext, sizeof plaintext / 2);                                                            \
        CHIP_aes_ctr_encrypt(&ctx, c + sizeof plaintext / 2, plaintext + sizeof plaintext / 2, sizeof plaintext / 2);              \
        CHIP_aes_ctr_done(&ctx);                                                                                                   \
        nlASSERT(!memcmp(c, ciphertext, sizeof c));                                                                                \
    }
#endif

static void test_store_big_endian(uint32_t v)
{
    uint8_t x[4];
    union
    {
        uint8_t x[4];
        uint32_t i;
    } u;
    CHIP_store_bigendian(x, v);
#if __BYTE_ORDER__ == __ORDER_LITTLE_ENDIAN__
    v = __builtin_bswap32(v);
#endif
    u.i = v;
    nlASSERT(!memcmp(x, u.x, sizeof x));
}

// This trips an assert if BN_bn2bin is used in the OpenSSL backend because the
// verifier has to be padded to use the full SRP_VERIFIER_BYTES width.
static void test_bn_pad()
{
    uint8_t v[SRP_VERIFIER_BYTES];
    uint8_t salt[SRP_SALT_BYTES];
    memset(salt, 0, sizeof salt);
    *(long *) salt = 217;
    CHIP_srp_verifier(v, salt, (const uint8_t *) "", 0, (const uint8_t *) "", 0);
}

