#define test_ed25519(sk, pk, m, sig)                                                                                               \
    {                                                                                                                              \
        uint8_t p[ED25519_PUBLIC_KEY_BYTES];                                                                                       \
        CHIP_ed25519_public_key(p, sk);                                                                                            \
        nlASSERT(!memcmp(p, pk, ED25519_PUBLIC_KEY_BYTES));                                                                        \
        uint8_t s[ED25519_BYTES];                                                                                                  \
        CHIP_ed25519_sign(s, m, sizeof m, sk, pk);                                                                                 \
        nlASSERT(!memcmp(s, sig, ED25519_BYTES));                                                                                  \
        nlASSERT(!CHIP_ed25519_verify(sig, m, sizeof m, pk));                                                                      \
        s[0] ^= 1;                                                                                                                 \
        nlASSERT(CHIP_ed25519_verify(s, m, sizeof m, pk) == -1);                                                                   \
        p[0] ^= 1;                                                                                                                 \
        nlASSERT(CHIP_ed25519_verify(s, m, sizeof m, p) == -1);                                                                    \
    }

