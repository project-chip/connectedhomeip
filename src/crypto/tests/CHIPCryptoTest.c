#define test_chacha20_poly1305(key, nonce, pt, aad, tag, ct)                                                                       \
    {                                                                                                                              \
        uint8_t t[CHACHA20_POLY1305_TAG_BYTES];                                                                                    \
        uint8_t c[300];                                                                                                            \
        size_t pt_len = sizeof pt - 1;                                                                                             \
        CHIP_chacha20_poly1305_encrypt_aad(t, c, pt, pt_len, aad, sizeof aad, nonce, sizeof nonce, key);                           \
        nlASSERT(!memcmp(c, ct, sizeof ct));                                                                                       \
        nlASSERT(!memcmp(t, tag, sizeof tag));                                                                                     \
        uint8_t m[300];                                                                                                            \
        int ret = CHIP_chacha20_poly1305_decrypt_aad(tag, m, ct, sizeof ct, aad, sizeof aad, nonce, sizeof nonce, key);            \
        nlASSERT(!ret);                                                                                                            \
        nlASSERT(!memcmp(m, pt, pt_len));                                                                                          \
        c[0] ^= 1;                                                                                                                 \
        ret = CHIP_chacha20_poly1305_decrypt_aad(tag, m, c, sizeof ct, aad, sizeof aad, nonce, sizeof nonce, key);                 \
        nlASSERT(ret == -1);                                                                                                       \
        t[0] ^= 1;                                                                                                                 \
        ret = CHIP_chacha20_poly1305_decrypt_aad(t, m, ct, sizeof ct, aad, sizeof aad, nonce, sizeof nonce, key);                  \
        nlASSERT(ret == -1);                                                                                                       \
    }

#define test_chacha20_poly1305_inc(key, nonce, pt, aad, tag, ct)                                                                   \
    {                                                                                                                              \
        uint8_t t[CHACHA20_POLY1305_TAG_BYTES];                                                                                    \
        CHIP_chacha20_poly1305_ctx ctx;                                                                                            \
        uint8_t c[300];                                                                                                            \
        uint8_t m[300];                                                                                                            \
        CHIP_chacha20_poly1305_init(&ctx, nonce, sizeof nonce, key);                                                               \
        CHIP_chacha20_poly1305_update_enc_aad(&ctx, aad, 1, nonce, sizeof nonce, key);                                             \
        CHIP_chacha20_poly1305_update_enc_aad(&ctx, aad + 1, sizeof aad - 1, nonce, sizeof nonce, key);                            \
        size_t pt_len = sizeof pt - 1;                                                                                             \
        for (size_t i = 0; i < pt_len; ++i)                                                                                        \
        {                                                                                                                          \
            CHIP_chacha20_poly1305_update_enc(&ctx, c + i, pt + i, 1, nonce, sizeof nonce, key);                                   \
        }                                                                                                                          \
        CHIP_chacha20_poly1305_final_enc(&ctx, t);                                                                                 \
        nlASSERT(!memcmp(c, ct, sizeof ct));                                                                                       \
        nlASSERT(!memcmp(t, tag, sizeof tag));                                                                                     \
        CHIP_chacha20_poly1305_init(&ctx, nonce, sizeof nonce, key);                                                               \
        CHIP_chacha20_poly1305_update_dec_aad(&ctx, aad, sizeof aad, nonce, sizeof nonce, key);                                    \
        for (int i = 0; i < sizeof ct; ++i)                                                                                        \
        {                                                                                                                          \
            CHIP_chacha20_poly1305_update_dec(&ctx, m + i, ct + i, 1, nonce, sizeof nonce, key);                                   \
        }                                                                                                                          \
        int ret = CHIP_chacha20_poly1305_final_dec(&ctx, t);                                                                       \
        nlASSERT(!ret);                                                                                                            \
        nlASSERT(!memcmp(m, pt, sizeof ct));                                                                                       \
        nlASSERT(!memcmp(t, tag, sizeof tag));                                                                                     \
    }                                                                                                                              \
