# ---------------------------------------------------------------------------
# Shared XML content helpers
# ---------------------------------------------------------------------------

# Body text used inside <!-- --> for a fully-valid Alchemy metadata comment.
_VALID_COMMENT_BODY = """\

XML generated by Alchemy; DO NOT EDIT.
Source: src/app_clusters/Foo.adoc
Parameters: zap --foo --bar
Git: 1.0-fall2025-42-gABCDEF1234567
Alchemy: v1.5.41
"""

_VALID_XML = f"""\
<?xml version="1.0"?>
<!--{_VALID_COMMENT_BODY}-->
<chip/>
"""


def _make_xml_file(content: str) -> Path:
    """Write *content* to a temporary file whose name is not in any allowlist."""
    fd, name = tempfile.mkstemp(suffix="-test-alchemy.xml")
    try:
        os.write(fd, content.encode("utf-8"))
    finally:
        os.close(fd)
    return Path(name)


# ---------------------------------------------------------------------------
# TestFindAlchemyComment
# ---------------------------------------------------------------------------

class TestFindAlchemyComment(unittest.TestCase):
    """Tests for the private _find_alchemy_comment() helper."""

    def test_returns_none_for_plain_xml(self):
        content = '<?xml version="1.0"?>\n<chip/>\n'
        self.assertIsNone(cam._find_alchemy_comment(content))

    def test_returns_none_for_comment_without_marker(self):
        content = '<!-- This is a regular XML comment -->\n<chip/>\n'
        self.assertIsNone(cam._find_alchemy_comment(content))

    def test_returns_comment_body_with_marker(self):
        content = f'<!--{_VALID_COMMENT_BODY}-->\n<chip/>\n'
        result = cam._find_alchemy_comment(content)
        self.assertIsNotNone(result)
        self.assertIn("XML generated by Alchemy", result)

    def test_finds_correct_comment_among_multiple(self):
        # First comment has no marker; second does.
        content = (
            '<!-- Just a header comment -->\n'
            f'<!--{_VALID_COMMENT_BODY}-->\n'
            '<chip/>\n'
        )
        result = cam._find_alchemy_comment(content)
        self.assertIsNotNone(result)
        self.assertIn("XML generated by Alchemy", result)


