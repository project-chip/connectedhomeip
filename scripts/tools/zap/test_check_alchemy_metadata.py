class TestCheckHandEdits(unittest.TestCase):
    """Tests for the private _check_hand_edits() helper.

    subprocess.run (for git diff) and _get_base_content are patched so
    no real git operations are performed.
    """

    def setUp(self):
        self._temp_dir = tempfile.mkdtemp()

    def tearDown(self):
        shutil.rmtree(self._temp_dir, ignore_errors=True)

    def _write_xml(self, content: str, name: str) -> Path:
        p = Path(self._temp_dir) / name
        p.write_text(content, encoding="utf-8")
        return p

    @staticmethod
    def _diff_result(changed_names: list[str]) -> MagicMock:
        """Return a mock CompletedProcess whose stdout lists the given names."""
        result = MagicMock()
        result.stdout = "\n".join(changed_names) + ("\n" if changed_names else "")
        return result

    def test_no_changed_files_returns_empty(self):
        p = self._write_xml(_VALID_XML, "foo-test-alchemy.xml")
        with patch("subprocess.run", return_value=self._diff_result([])):
            errors = cam._check_hand_edits([p], Path(self._temp_dir), "main")
        self.assertEqual(errors, [])

    def test_git_diff_failure_returns_error(self):
        p = self._write_xml(_VALID_XML, "foo-test-alchemy.xml")
        exc = subprocess.CalledProcessError(1, "git")
        with patch("subprocess.run", side_effect=exc):
            errors = cam._check_hand_edits([p], Path(self._temp_dir), "main")
        self.assertEqual(len(errors), 1)
        self.assertIn("unable to run git diff", errors[0])

    def test_changed_file_same_metadata_flagged(self):
        # New and old files have identical Alchemy comment → hand-edit.
        fname = "bar-test-alchemy.xml"
        p = self._write_xml(_VALID_XML, fname)
        with (
            patch("subprocess.run", return_value=self._diff_result([fname])),
            patch("check_alchemy_metadata._get_base_content",
                  return_value=_VALID_XML),
        ):
            errors = cam._check_hand_edits([p], Path(self._temp_dir), "main")
        self.assertEqual(len(errors), 1)
        self.assertIn("suspected hand-edit", errors[0])

    def test_changed_file_different_metadata_passes(self):
        # Alchemy comment differs between base and new → legitimate regen.
        fname = "baz-test-alchemy.xml"
        p = self._write_xml(_VALID_XML, fname)
        old_content = (
            '<?xml version="1.0"?>\n'
            '<!--\n'
            'XML generated by Alchemy; DO NOT EDIT.\n'
            'Source: src/app_clusters/Bar.adoc\n'
            'Parameters: zap --old-flag\n'
            'Git: 1.0-fall2025-1-gDEADBEEF1234\n'
            'Alchemy: v1.5.40\n'
            '-->\n'
            '<chip/>\n'
        )
        with (
            patch("subprocess.run", return_value=self._diff_result([fname])),
            patch("check_alchemy_metadata._get_base_content",
                  return_value=old_content),
        ):
            errors = cam._check_hand_edits([p], Path(self._temp_dir), "main")
        self.assertEqual(errors, [])

    def test_changed_file_in_manual_allowlist_skipped(self):
        fname = "qux-test-alchemy.xml"
        p = self._write_xml(_VALID_XML, fname)
        with (
            patch("subprocess.run", return_value=self._diff_result([fname])),
            patch.object(cam, 'MANUAL_ALLOWLIST', {fname}),
        ):
            errors = cam._check_hand_edits([p], Path(self._temp_dir), "main")
        self.assertEqual(errors, [])

    def test_changed_new_file_no_base_passes(self):
        # File is new (no base content) → not a hand-edit.
        fname = "new-test-alchemy.xml"
        p = self._write_xml(_VALID_XML, fname)
        with (
            patch("subprocess.run", return_value=self._diff_result([fname])),
            patch("check_alchemy_metadata._get_base_content", return_value=None),
        ):
            errors = cam._check_hand_edits([p], Path(self._temp_dir), "main")
        self.assertEqual(errors, [])

    def test_changed_file_no_alchemy_comment_skipped(self):
        # File has no Alchemy comment in the new version → skipped by design
        # (the marker-absence check in validate_file handles this separately).
        fname = "plain-test-alchemy.xml"
        p = self._write_xml('<?xml version="1.0"?>\n<chip/>\n', fname)
        with patch("subprocess.run", return_value=self._diff_result([fname])):
            errors = cam._check_hand_edits([p], Path(self._temp_dir), "main")
        self.assertEqual(errors, [])

    def test_get_base_content_called_with_root_as_cwd(self):
        # Regression test: _get_base_content must receive root so that git show
        # runs against the correct repository when --root differs from cwd.
        fname = "root-cwd-test-alchemy.xml"
        root = Path(self._temp_dir)
        p = self._write_xml(_VALID_XML, fname)
        with (
            patch("subprocess.run", return_value=self._diff_result([fname])),
            patch("check_alchemy_metadata._get_base_content",
                  return_value=_VALID_XML) as mock_get_base,
        ):
            cam._check_hand_edits([p], root, "main")
        # Verify root was threaded through to _get_base_content.
        mock_get_base.assert_called_once_with("main", fname, root)


# ---------------------------------------------------------------------------
# Entry point (supports --stamp for build system integration)
# ---------------------------------------------------------------------------

def main() -> int:
    parser = argparse.ArgumentParser(add_help=False)
    parser.add_argument(
        "--stamp",
        default=None,
        metavar="FILE",
        help="Write this file on success (for build system stamp integration).",
    )
    args, remaining = parser.parse_known_args()
    sys.argv[1:] = remaining

    loader = unittest.TestLoader()
    suite = loader.loadTestsFromModule(sys.modules[__name__])
    runner = unittest.TextTestRunner(verbosity=2)
    result = runner.run(suite)

    if result.wasSuccessful() and args.stamp:
        with open(args.stamp, "w", encoding="utf-8") as f:
            f.write("SUCCESS\n")

    return 0 if result.wasSuccessful() else 1


if __name__ == "__main__":
    sys.exit(main())
